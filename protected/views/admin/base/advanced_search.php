<?php

if (!isset($listId)) $listId = 'user-grid';

Yii::app()->clientScript->registerScript('search', "
$('.search-button').click(function(){
    $('.search-form').toggle();
    return false;
});
$('.search-form form').submit(function(){
    $.fn.yiiGridView.update('{$listId}', {
        data: $(this).serialize()
    });
    return false;
});
");
?>

